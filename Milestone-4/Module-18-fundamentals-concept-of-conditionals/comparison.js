// comparison Operators
// 1.grater than: >
console.log(5 > 4);

// 2.less than: <
console.log(5 < 7);

// 3.equal: ==
console.log(5 == 5);

// 4.greater than or equal to: >=
console.log(5 >= 4);

// 5.less than or equal to: <=
console.log(5 <= 4);

// 6. not equal: !=
console.log(5 != 6);

// 7. and operator: &&
console.log(5 < 6 && 6 < 44);

// 8. or operator: ||
console.log(5 < 6 || 6 < 44);

// 9. not operator: !
console.log(!true);
