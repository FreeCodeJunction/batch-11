var day = "Monday";
switch (day) {
  case "Saturday":
    console.log("It is saturday");
    break;
  case "Sunday":
    console.log("It is sunday");
    break;
  case "Monday":
    console.log("It is Monday");
    break;
  case "Tuesday":
    console.log("It is Tuesday");
    break;
  default:
    console.log("none of the above");
}
